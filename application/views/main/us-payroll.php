                            earnings after deductions. They contain the total number of hours served by the employee,
                            the pay period, and the dates. Payslips also have the following information:</span></p>
                        <ul style="padding: 15px;">
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
