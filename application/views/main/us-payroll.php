                        forming a subsidiary in the United States. When you file your subsidiary, you will need to get
                        an Employer Identification Number (EIN) to comply with federal tax laws.
                        <p class="mb-0 mt-10">
                          <span class="us_paragraph"> Payroll tax is a state-based levy, so the phase of setting up
                            payroll can differ depending on which state the business is registered in.</span>
