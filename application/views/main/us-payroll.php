                        <ul style="padding: 10px;">                          
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            <span class="us_sub_heading_highlights">USA Pay slips  </span> - Although not stated by law, it is common for employers to provide employees with paper or electronic pay slips and it will provide via email as well. 
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            <span class="us_sub_heading_highlights">Pay slip Deductions  </span> - An employer is required to make a range of necessary deductions in order to comply with the legislation. There are the following:
                            <ul style="padding: 15px;">
                              <li style="list-style: disc;padding: 2px;font-size: 14px;">
                                <span class="us_sub_heading_highlights"> Social security and Medicare insurance</span> – As per Federal Insurance Contributions Act (FICA) employers must withhold Social Security and Medicare insurance from workers' salaries. 
                              </li>
                              <li style="list-style: disc;padding: 2px;font-size: 14px;">
                                <span class="us_sub_heading_highlights">Income Tax</span> – Both Federal and State income taxes must be withheld from gross pay on the pay slips before an employee is paid. 
                              </li>                             
                              </ul>
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            <span class="us_sub_heading_highlights">Sick Pay  </span> - Several states mandate that it be made available to employees, and others don’t. This varies by state-to-state and employer-to-employer.
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            <span class="us_sub_heading_highlights">Maternity Pay</span> - Expectant mothers are covered by the Family and Medical Leave Act (FMLA), which allows them to take up to 12 weeks off without losing the job. Companies are not obligated to pay them during this period. 
                          
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            <span class="us_sub_heading_highlights">Payroll Record-keeping </span> - The payroll records of an employee must be held for 3-years, according to federal law. However, some states, such as California, New York, and Washington laws require a six-year retention of payroll records. 
                            
                          </li> </ul>
                      </li></ul>
                  </span>
