                    <li style="list-style: disc;padding: 2px;font-size: 14px;">
                      <span class="us_sub_heading_highlights">Overtime Pay  </span> - Employers might have to pay 1.5 times the normal rate. Employees who work 40-hours /week, are entitled to extra pay, according to the Fair Labor Standards Act (FLSA). 
                    </li>
                    <li style="list-style: disc;padding: 2px;font-size: 14px;">
                      <span class="us_sub_heading_highlights">Vacation Leave </span> - There is no federal law requiring employers to provide paid vacation. But employers usually provide about 10 days a year.
                      
                    </li>
                    <li style="list-style: disc;padding: 2px;font-size: 14px;">
                      <span class="us_sub_heading_highlights">Termination Pay  </span> - Employees whose contracts have been terminated in many states are required to be compensated for any unused time off (time in lieu) that they have received prior to their termination. 
                    </li>
                    </ul>
