                            Employee’s details and check date and number
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            Net salary, Current and Year to Date (YTD) Totals including gross earnings, and/or any
                            deductions.
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            All types of earnings
                          </li>
                          <li style="list-style: disc;padding: 2px;font-size: 14px;">
                            Employee Taxes that have been deducted.
