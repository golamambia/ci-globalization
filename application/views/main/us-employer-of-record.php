              taxable income and filing status. Major tax brackets are - 10%, 12%, 22%, 24%, 32%, 35% and 37%.
              Currently, there is no national sales tax in the US and therefore no standard rate for the same. Usually,
              the sales tax rates differ by state ranging from 2.9 to 7.25% at the state level.
            </span>
          </p>

