                <h3 class="elementor-heading-title-2  elementor-size-default"><span id="Type_US_VISA">How to obtain US
                    Work Visas </span>
                </h3>
                <p class="mb-0">
                  <span class="us_paragraph">
                    To obtain US work visas, it is the responsibility of the employer to file a petition for each
                    employee who will require an H-IB visa. Also, there is a certain application process that needs to
                    be followed here. Below are the steps need to be followed -
                  </span>

                <ul style="padding: 15px;">
                  <li style="list-style: disc;padding: 5px;">
                    <span class="us_sub_heading_highlights">Filing petition </span> - The first step to obtain a US work
                    visa is to include a variety of job-specific data which will include the exact dates of employment
                    and responsibilities of the job position.
                  </li>
                  <li style="list-style: disc;padding: 5px;">
                    <span class="us_sub_heading_highlights">Determining prevailing wage and actual wage </span> - The
                    employer is required to file a form with the State Employment Security Agency in the United States
                    to identify the prevailing wage in the area. the actual which can be determined by comparing the
                    wages of other workers in the same job role with the same experience level as the employee and the
                    company. Hence, the employer must pay the higher of the two.
                  </li>
                  <li style="list-style: disc;padding: 5px;">
                    <span class="us_sub_heading_highlights">Filing a Labor Certification Application (LCA) </span> - The
                    company agrees to pay the previously determined wage and offer the international employee the same
                    benefits as other employees by signing this form.
                  </li>
                  <li style="list-style: disc;padding: 5px;">
                    <span class="us_sub_heading_highlights">Approval of LCA </span> - Upon completing the other
                    procedures, the company will receive a certified copy of the labor certification application from
                    the US Department of Labour. It is also required for the company to post a notice at the worksite
                    that the company has applied for the LCA.
                  </li>
                  <li style="list-style: disc;padding: 5px;">
                    <span class="us_sub_heading_highlights">Filing petition with the US Citizenship and Immigration
                      services </span> - Upon completion of the above procedures the company will obtain a receipt with
                    a reference number. This is then followed by receiving a notice after which the employee can start
                    working for the required company.
                  </li>
                </ul>
                </p>
